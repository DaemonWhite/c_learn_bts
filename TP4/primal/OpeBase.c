float addition(float x,float y)
{
	float somme;
	somme = x+y;
	
	return somme;
}

float soustraction(float x,float y)
{
	float somme;
	somme = x-y;
	
	return somme;
}

float multi(float x,float y)
{
	float somme;
	somme = x*y;
	
	return somme;
}

float div(float x,float y)
{
	float somme;
	somme = x/y;
	
	return somme;
}
